# ------------------------------------------------------------------------------------
# This script only works for unadaptive meshes
# ------------------------------------------------------------------------------------

if [ "$#" -ne 3 ]; then
    echo "==========================================================================="
    echo "Usage:> ./getAps.sh <directory_name> <number_id_cell> <output_file>"
    echo "==========================================================================="
    exit 1
fi

AP_DIR=$1
AP_N=$2

for i in `ls -1v ${AP_DIR}/V_t_*`; do sed -n "${AP_N}p" $i | awk -F ',' '{print $5}'  ; done > ${3}.txt
